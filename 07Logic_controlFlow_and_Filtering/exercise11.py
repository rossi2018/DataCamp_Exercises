z=3
if z% 2==0:
    print('z is divisible by 2') # False 

elif z%3==0:     #The elif keyword is pythons way of saying "if the previous conditions were not true, then try this condition
    print('z is divisible by 3')  # True

else:
    print('z is neither divisible by 2 nor by 3 ')

#the summary is, if z equals 3, the first condition is false,
#so it goes over to the next condition. This condition, does hold, 
# so the corresponding print statement is executed



    